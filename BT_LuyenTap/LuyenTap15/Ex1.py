'''
Thực hiện các phép toán cơ bản với hai số. Nhập hai số nguyên từ người dùng. Tính và in ra tổng, hiệu, 
tích và thương của chúng
'''

a = int(input('Vui lòng nhập số nguyên thứ nhất: '))
b = int(input('Vui lòng nhập số nguyên thứ hai: '))

print(f'Tổng của 2 số nguyên là: {a+b}')
print(f'Hiệu của 2 số nguyên là: {a-b}')
print(f'Tích của 2 số nguyên là: {a*b}')
print(f'Thương của 2 số nguyên là: {a/b}')
